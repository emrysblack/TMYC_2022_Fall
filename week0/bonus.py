def execute_intcodes(codes):
    
    # mode helper function
    def get_args_with_mode(argc, ptr, codes):
        args = codes[ptr+1:ptr+argc+1] # get instruction
        # mode
        mode = str(codes[ptr]).zfill(argc+2)[-3::-1]
        return list(map(lambda x, y: x if y == '1' else codes[x], args, mode))

    # register functions 
    def add(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        out = codes[ptr + 3]

        codes[out] = x + y
        return 4
    
    def mul(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        out = codes[ptr + 3]

        codes[out] = x * y
        return 4
    
    def prompt(ptr, codes):
        out = codes[ptr+1]
        codes[out] = int(input())
        return 2
    
    def output(ptr, codes):
        x, = get_args_with_mode(1, ptr, codes)
        print(x)
        return 2

    def jump_if_true(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        return y - ptr if x != 0 else 3

    def jump_if_false(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        return y - ptr if x == 0 else 3
    
    def less_than(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        out = codes[ptr + 3]

        codes[out] = int(x < y)
        return 4
    
    def equal(ptr, codes):
        x,y = get_args_with_mode(2, ptr, codes)
        out = codes[ptr + 3]

        codes[out] = int(x == y)
        return 4
    
    # assign functions to codes
    optcodes = { 1: add, 2: mul, 3: prompt, 4: output, 5: jump_if_true,
                 6: jump_if_false, 7: less_than, 8: equal }
    
    # run program
    i = 0
    while (codes[i] != 99): # 99 quit code
        i += optcodes[codes[i]%10](i, codes)
        

